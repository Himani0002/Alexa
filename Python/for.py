# printing number using range

print("number using for loop\n")
for i in range(5):
  if(i==3):
    continue
  print(i+1)

# Set and Array printing

a=[1,34,24,23,23]

print("printing array using for loop\n")
for item in a:
  print(item)
  
s={3,2,34,32,1,5}

print("printing set using for loop\n")
for item in s:
  print(item)